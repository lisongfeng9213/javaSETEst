
public class DataLine {
	double []x;
	int y;
	public DataLine(double[] x2, int y) {
		super();
		this.x = x2;
		this.y = y;
	}
	public double[] getX() {
		return x;
	}
	public void setX(double[] x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
}
